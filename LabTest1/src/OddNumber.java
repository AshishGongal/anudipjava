
public class OddNumber {

	public static void main(String[] args) {
		int number=20;  
		System.out.print("List of odd numbers from 1 to "+number+": ");  
		for (int i=1; i<=number; i++)   
		{   
		if (i%2!=0)  //if i%2 is not equal to zero, the number is odd   
		{  
		System.out.print(i + " ");  
		}  
		}  

	}

}
